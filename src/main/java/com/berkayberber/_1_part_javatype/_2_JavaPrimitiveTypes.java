package com.berkayberber._1_part_javatype;

public class _2_JavaPrimitiveTypes {
    public static void main(String[] args) {
        // Primitive Types 8 tanedir. null değer alınamaz
        // Tam sayılar (4)
        byte b1 = 127;
        System.out.println("byte b1 = " + b1);
        short s1 = 31231;
        System.out.println("short s1 = " + s1);
        int i1 = 1213313123;
        System.out.println("int = " + i1);
        long l1 = 1213313123L;
        System.out.println("long = " + l1);

        // Virgüllü Sayılar (floating point)

        float f2 = 3.14f;
        System.out.println("float = " + f2);
        double d1 = 3.14;
        System.out.println("double = " + d1);

        // Boolean

        boolean b2 = true;
        System.out.println("boolean = " + b2);

        // char

        char c1 = 'ﺐ'; //
        System.out.println("char = " + c1);
    }
}
